			<h1>Concepto</h1>
            Caliunderground es por excelencia el evento que busca resaltar el talento original de la musica rock de Cali, se llama de esta forma gracias a que cada año logra reunir talentos escondidos de las culturas rockeras que se desarrollan en las comunas de Cali.<br>
<br>
El respeto siempre ha sido el eje central de este trabajo, no nos importa si el artista es punkero, metalero, alternativo lo importante es que cada uno de nosotros merece ser escuchado y el compartir los sonidos nos acerca y nos une entorno a espacios de reflexión individual y colectiva generando grandes transformaciones sociales.
<br>
<br>
Durante 6 versiones, Caliunderground fue un concierto que se ajustaba al modelo de conciertos del pais, con algunas diferencias conceptuales y de organización pero estabamos cerca a esa idea. A partir del surgimiento de la union de los emprendimientos culturales del rock y del compartir del presupuesto destinado para los procesos de esta cultura, hemos diseñado un concepto diferente que nos acerca a la nueva realidad presupuestal y que nos permite con el uso de las nuevas tecnologias proyectar un alcance mayor de publicos y de participación de artistas en lo que es el nuevo Caliunderground.<br>
<br>
Desde ahora en adelante cada año Caliunderground reunirá diferentes agrupaciones bajo un hilo conceptual y con ellas trabajará durante 4 meses para lograr una producción musical en vivo impecable registrada en DVD. Tendremos dos dias de concierto cada uno con 5 agrupaciones donde las personas podran asistir pero alli mismo lo importante mas que el hecho del concierto será la producción audiovisual documental que contará una historia donde los mayores beneficiados serán las agrupaciones participantes.<br>
<br>
De esta producción audiovisual se imprimiran en DVD con su respectiva caratula 1000 copias, de las cuales 100 se utilizarán para ser enviadas a distintas personalidades del rock nacional asi como medios de comunicación y patrocinadores. Cada banda recibira 50 copias de este DVD y podrán comercializarlo o utilizarlo como material promocional.<br>
<br>
Adicional a esto cada banda participante recibira un pago por su presentación ademas de toda la cobertura publicitaria y de medios que genere el proceso.<br>
<br>
Para llegar al dia del concierto tendremos una narración de los acontecimientos que van sucediendo desde esta pagina de internet, a manera de reallity, donde se contara paso a paso el proceso que nos lleva a la producción final y lo que sucede despues de esta. Tendremos entrevistas a diferentes personalidades del rock asi como a funcionarios publicos y privados que le apuestan al desarrollo del rock. Seran 10 capitulos uno cada 15 dias de entre 5 y 10 minutos de duración en una producción original nunca antes vista en Colombia y dedicada al rock nacional.<br>
<br>
Este año la historia que se desarrollara tendra como eje principal la historia de Caliunderground, ya que son 11 años de trabajo y dedicación y queremos narrar un poco como ha sido este proceso y hacia donde se dirige, con musica, con rock y con mucha energia!<br>
